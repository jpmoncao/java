/*
(for) EXERCÍCIO 3: Escreva um programa que imprima o quadrado dos números no intervalo fechado de 1 a 20.
 */
package quadradodeumavinte;

public class QuadradoDeUmAVinte {

    public static void main(String[] args) {
        for (int c = 1; c <= 20; c++) {
            System.out.println(c+"² = "+c*c);
        }
    }
    
}
